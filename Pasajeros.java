public class Pasajeros {
    
    String NombredelPasajero;
    String NumerodeAsiento;
    String MarcadeAvion;
    String Pasaporte;
    String Clase;



  public Pasajeros() {
    }

    public String getNombredelPasajero() {
        return NombredelPasajero;
    }

    public void setNombredelPasajero(String NombredelPasajero) {
        this.NombredelPasajero = NombredelPasajero;
    }

    public String getNumerodeAsiento() {
        return NumerodeAsiento;
    }

    public void setNumerodeAsiento(String NumerodeAsiento) {
        this.NumerodeAsiento = NumerodeAsiento;
    }

    public String getMarcadeAvion() {
        return MarcadeAvion;
    }

    public void setMarcadeAvion(String MarcadeAvion) {
        this.MarcadeAvion = MarcadeAvion;
    }

    public String getPasaporte() {
        return Pasaporte;
    }

    public void setPasaporte(String Pasaporte) {
        this.Pasaporte = Pasaporte;
    }

    public String getClase() {
        return Clase;
    }

    public void setClase(String Clase) {
        this.Clase = Clase;
    }

    
}

   
    
    
  

